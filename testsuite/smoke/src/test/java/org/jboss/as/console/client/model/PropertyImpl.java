package org.jboss.as.console.client.model;

import org.jboss.as.console.client.shared.properties.PropertyRecord;

/**
 * @author Heiko Braun
 * @date 4/29/11
 */
public class PropertyImpl implements PropertyRecord {
    
    String key;
    String value;
    
    boolean isBootTime;
    
    @Override
    public String getKey() {
        return key;
    }

    @Override
    public void setKey(String key) {
        this.key = key;
    }

    @Override
    public String getValue() {
        return value;
    }

    @Override
    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public void setBootTime(boolean b) {
        this.isBootTime = b;
    }

    @Override
    public boolean isBootTime() {
        return this.isBootTime;
    }
}
